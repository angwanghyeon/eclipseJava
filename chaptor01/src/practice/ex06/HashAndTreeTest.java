package practice.ex06;

import java.util.Arrays;
import java.util.HashSet;
import java.util.Set;
import java.util.TreeSet;

public class HashAndTreeTest {
	public static void main(String[] args) {
		Set<String> s1 = new HashSet<>();
		s1.addAll(Arrays.asList("독수리","나비","염소","고양이","개미","여우"));
		System.out.println("HashSet : "+s1);
		TreeSet<String> ts = new TreeSet<>(s1);
		System.out.println("TreeSet : "+ts);
		System.out.println("TreeSet의 first메소드 : "+ts.first());
		System.out.println("TreeSet의 last메소드 : "+ts.last());
		System.out.println("TreeSet의 lower메소드 : "+ts.lower("독수리"));
		
	}
}
